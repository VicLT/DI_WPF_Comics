--- Create database
USE master
GO
if exists (select * from sysdatabases where name='Comics')
		drop database Comics
go

DECLARE @device_directory NVARCHAR(520)
SELECT @device_directory = SUBSTRING(filename, 1, CHARINDEX(N'master.mdf', LOWER(filename)) - 1)
FROM master.dbo.sysaltfiles WHERE dbid = 1 AND fileid = 1

EXECUTE (N'CREATE DATABASE Comics
  ON PRIMARY (NAME = N''EEF'', FILENAME = N''' + @device_directory + N'Comics.mdf'')
  LOG ON (NAME = N''EEF_log'',  FILENAME = N''' + @device_directory + N'Comics.ldf'')')
go

USE Comics
go

CREATE TABLE Autor (
    AutorID int IDENTITY (1, 1) PRIMARY KEY,
    Nombre varchar(60),
    Apellido varchar(100),
    Pais varchar(100)
);

CREATE TABLE Editorial (
    EditorialID int IDENTITY (1, 1) PRIMARY KEY,
    Nombre varchar(60)
);

CREATE TABLE Comic (
    ComicID int IDENTITY (1, 1) PRIMARY KEY,
    Nombre varchar(150),
    AutorID int,
    EditorialID int,
	Precio_Compra DECIMAL(10, 2),
	Precio_Venta DECIMAL(10, 2),
    FOREIGN KEY (AutorID) REFERENCES autor(AutorID),
    FOREIGN KEY (EditorialID) REFERENCES editorial(EditorialID)
);

CREATE TABLE Empleado (
    EmpleadoID int IDENTITY (1, 1) PRIMARY KEY,
    Nombre varchar(60),
    Apellido varchar(100),
    NIF varchar(10) NOT NULL,
    Direccion varchar(255),
    Password varchar(64) -- para almacenar el hash SHA-256 de la contraseña
);

CREATE TABLE Local (
    LocalID int IDENTITY (1, 1) PRIMARY KEY,
    Nombre varchar(60),
    Direccion varchar(255),
    Latitud DECIMAL(9, 6) NOT NULL,
    Longitud DECIMAL(9, 6) NOT NULL
);

CREATE TABLE Local_Empleado (
    EmpleadoID int NOT NULL,
    LocalID int NOT NULL,
	PRIMARY KEY (EmpleadoID,LocalID),
	FOREIGN KEY (EmpleadoID) REFERENCES Empleado(EmpleadoID),
    FOREIGN KEY (LocalID) REFERENCES Local(LocalID));


CREATE TABLE Stock_Comic (
    Stock_ComicID int IDENTITY (1, 1) PRIMARY KEY,
    ComicID int NOT NULL,
    LocalID int NOT NULL,
    Cantidad int,
    FOREIGN KEY (LocalID) REFERENCES local(LocalID),
    FOREIGN KEY (ComicID) REFERENCES comic(ComicID)
);

CREATE TABLE Medio_De_Pago (
    Medio_De_PagoID int IDENTITY (1, 1) PRIMARY KEY,
    Descripcion varchar(255),
    Nombre_Corto varchar(25)
);

CREATE TABLE Tipo_Operacion (
    Tipo_OperacionID int IDENTITY (1, 1) PRIMARY KEY,
    Descripcion varchar(255)
);

CREATE TABLE Operacion (
    OperacionID int IDENTITY (1, 1) PRIMARY KEY,
    Medio_De_PagoID int NOT NULL,
    Tipo_OperacionID int NOT NULL,
    ComicID int NOT NULL,
    LocalID int NOT NULL,
    Fecha_Operacion DATETIME NOT NULL,
	EmpleadoID int NOT NULL,
    FOREIGN KEY (Medio_De_PagoID) REFERENCES medio_de_pago(Medio_De_PagoID),
    FOREIGN KEY (Tipo_OperacionID) REFERENCES tipo_operacion(Tipo_OperacionID),
    FOREIGN KEY (ComicID) REFERENCES comic(ComicID),
    FOREIGN KEY (LocalID) REFERENCES local(LocalID),
	FOREIGN KEY (EmpleadoID) REFERENCES Empleado(EmpleadoID));


CREATE TABLE Detalle_Operacion (
    Detalle_OperacionID int IDENTITY (1, 1) PRIMARY KEY,
    OperacionID int NOT NULL,
    ComicID int NOT NULL,
    Cantidad int,
    Precio DECIMAL(10, 2),
	Descuento DECIMAL(7, 4),
    FOREIGN KEY (OperacionID) REFERENCES operacion(OperacionID),
    FOREIGN KEY (ComicID) REFERENCES comic(ComicID)
);

